package practice;

public class encapsulation {
	
	private int rollno;
	private String empname;
	
    public int getrollno() {
    	return rollno;
    }
    
    public String getempname() {
    	return empname;
    }
    
    public void setempname(String empname) {
    	this.empname=empname;
    }
    
    public void setrollno(int rollno) {
    	this.rollno=rollno;
    }

}
